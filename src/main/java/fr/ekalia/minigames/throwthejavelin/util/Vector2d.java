package fr.ekalia.minigames.throwthejavelin.util;

/**
 * @author roro1506_HD
 */
public class Vector2d {

    private final double x;
    private final double y;

    public Vector2d(double x, double y) {
        this.x = x;
        this.y = y;
    }

    public double getX() {
        return this.x;
    }

    public double getY() {
        return this.y;
    }
}
